

class Solution {
	public static void main (String []args){
		        int []nums={2,7,11,15};
		        int target=0;
		     twoSum(nums,target);
		    }
		    public static int[] twoSum(int[] nums, int target) {
		      
		       for(int i=0;i<nums.length;i++){
		           target=target+nums[i];

		       } 
		       return(nums);
		    }
		    
		

}
